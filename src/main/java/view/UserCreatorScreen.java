    private void iniciar(UserBEAN ub) {
        setVisible(false);
        ConfigBEAN cb = new ConfigFileFactory().readFile();
        boolean opcs[] = cb.getOptions();
        if (chbs[1]) {
            cb.setSavedUser(ub);
        }
        opcs[1] = chbs[1];
        opcs[2] = chbs[0];
        cb.setOptions(opcs);
        new UserCONTROL().add(ub);
        try {
            Thread.sleep(3000);
        } catch (Exception e) {
        }
        new ConfigFileFactory().writeFile(cb);
        System.out.println("[XLIV-INFO] Atualizando Arquivo De Configurações");
        new ConfigFileFactory().writeFile(cb);
        System.out.println("[XLIV-INFO] Iniciando Login Middleware");
        new LoginMiddleware().login();
        dispose();
    }
