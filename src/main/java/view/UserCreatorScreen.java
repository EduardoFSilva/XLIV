    private void btnCarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarregarActionPerformed
        UserBEAN ub = new UserBEAN();
        ub.setName(tfName.getText());
        ub.setEmail(tfEmail.getText());
        ub.setNick(tfNick.getText());
        ub.setRole("admin");
        String pass1 = Encriptar.toMD5(new String(tfPass.getPassword()));
        String pass2 = Encriptar.toMD5(new String(tfConfPass.getPassword()));
        if (pass1.equals(pass2)) {
            ub.setPassword(pass1);
            iniciar(ub);
        } else {
            JOptionPane.showMessageDialog(this, "Senhas São Diferentes", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCarregarActionPerformed

